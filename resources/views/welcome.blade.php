<script>
let slideIndex = 1;
shoSlides(slideIndex);

function plusSlides(n) {
  shoSlides(slideIndex += n);
}

function currentSlid(n) {
  shoSlides(slideIndex = n);
}

function shoSlides(n) {
  let i;
  let slid = document.getElementsByClassName("mySli");
  let dots = document.getElementsByClassName("demo");
  let captionText = document.getElementById("kaption");
  if (n > slid.length) {slideIndex = 1}
  if (n < 1) {slideIndex = slid.length}
  for (i = 0; i < slid.length; i++) {
    slid[i].style.display = "none";
  }
  for (i = 0; i < dots.length; i++) {
    dots[i].className = dots[i].className.replace(" active", "");
  }
  slid[slideIndex-1].style.display = "block";
  dots[slideIndex-1].className += " active";
  captionText.innerHTML = dots[slideIndex-1].alt;
}
